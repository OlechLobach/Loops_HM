початок = int(input("Введіть початок діапазону: "))
кінець = int(input("Введіть кінець діапазону: "))
print("Результат аналізу чисел у заданому діапазоні:")

for число in range(початок, кінець + 1):
    результат = ""
    if число % 3 == 0:
        результат += "Fizz"
    if число % 5 == 0:
        результат += "Buzz"

    if результат:
        print(f"{число}: {результат}")
    else:
        print(число)
